package cloudfoundry.norouter.config;

import org.hibernate.validator.constraints.NotEmpty;
import org.springframework.boot.context.properties.ConfigurationProperties;

/**
 * @author Mike Heath
 */
@ConfigurationProperties("f5.routerVirtualServer")
public class F5RouterVipProperties {

	@NotEmpty
	private String name = "zz_cf_router";
	@NotEmpty
	private String destination;
	private String description = "Cloud Foundry Norouter Virtual Server -- Automatically created by Norouter.";

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDestination() {
		return destination;
	}

	public void setDestination(String destination) {
		this.destination = destination;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}
}
